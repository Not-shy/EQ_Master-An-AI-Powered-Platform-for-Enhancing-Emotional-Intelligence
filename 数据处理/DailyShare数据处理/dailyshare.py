import json


def convert_to_json(data):
    lines = data.strip().split('\n')
    result = []
    for line in lines:
        parts = line.split('  ')
        if len(parts) != 12:
            continue  # 跳过不完整的数据行
        entry = {
            "id": parts[0],
            "createdAt": parts[1],
            "updatedAt": "",  # 假设updatedAt总是空的
            "isLike": bool(int(parts[2])),  # 假设isLike是0或1
            "isStar": False,  # 假设isStar总是false
            "isShare": False,  # 假设isShare总是false
            "type": parts[5],
            "title": parts[6],
            "context": parts[3],
            "picture": parts[4],
            "likes": int(parts[7]),
            "view": int(parts[8]),
            "commentCount": int(parts[9]),
            "favorite": int(parts[10]),
            "share": int(parts[11])
        }
        result.append(entry)
    return result


